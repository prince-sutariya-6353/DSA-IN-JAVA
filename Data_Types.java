public class Data_Types {
    public static void main(String[] args) {
        int a = 10;
        double b = 3.14;
        char c = 'A';
        String d = "hii";
        boolean e = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
